public class Conversao {
    public static void main (String[] args){
        //Declaração de variável e atribuição de valor de inicialização
        double salario = 123.56;
        //Declaração de variável e atribuição de valor convertido via casting
        int valor = (int) salario;
        //Exibição do valor das variáveis
        System.out.println("O salario double e " + salario + " e convertido em int e " + valor);
    }
}
