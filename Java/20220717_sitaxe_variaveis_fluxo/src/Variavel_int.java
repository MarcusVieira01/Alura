public class Variavel_int {
    public static void main(String[] args){
        //Declaração de variável e atribuição de valor incial
        int idade = 37;
        int resultado = idade / 2; //O valor de resultado será um valor truncado, apenas da porção real do resultado
        //Exibição concatenada dosvalores das variáveis
        System.out.println("A idade e " + idade);
        System.out.println("A idade truncada e " + resultado);        
    }
}
