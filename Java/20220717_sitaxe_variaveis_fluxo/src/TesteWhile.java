public class TesteWhile {
    //Declaração de método main
    public static void main(String[] args) {
        //Declaração de variável de controle e atribuição do valor de inicialização
        int contador = 0;
        //Declaração de loop while com variável de controle na comparação de controle
        while(contador < 10){
            //Exibição do valor da variável contador
            System.out.println(contador);
            //Incremento de 1 à variável contador
            contador ++;
        }
    }
}
